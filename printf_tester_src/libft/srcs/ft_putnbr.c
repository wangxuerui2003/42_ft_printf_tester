#include "libft.h"

void	ft_putnbr(int num)
{
	char	c;

	c = 0;
	if (num == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (num < 0)
	{
		num *= -1;
		write(1, "-", 1);
	}
	if (num > 9)
	{
		ft_putnbr(num / 10);
		ft_putnbr(num % 10);
	}
	else
	{
		c = num + 48;
		write(1, &c, 1);
	}
}
